int	ft_add(int i, int j)
{
	long li;
	long lj;

	li = i;
	lj = j;
	return (li + lj);
}

int	ft_minus(int i, int j)
{
	long li;
	long lj;

	li = i;
	lj = j;
	return (li - lj);
}

int	ft_multiply(int i, int j)
{
	long li;
	long lj;

	li = i;
	lj = j;
	return (li * lj);
}

int	ft_divide(int i, int j)
{
	long li;
	long lj;

	li = i;
	lj = j;
	return (li / lj);
}

int	ft_mod(int i, int j)
{
	long li;
	long lj;

	li = i;
	lj = j;
	return (li % lj);
}

void	ft_sort_int_tab(int *tab, int size)
{
	int a;
	int b;
	int swap;

	a = 0;
	while (a <= (size - 2))
	{
		b = a + 1;
		while (b <= (size - 1))
		{
			if (*(tab + a) > *(tab + b))
			{
				swap = *(tab + a);
				*(tab + a) = *(tab + b);
				*(tab + b) = swap;
			}
			b++;
		}
		a++;
	}
}
